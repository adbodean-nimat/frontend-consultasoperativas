import { getToken } from "@/services/auth";
import { decodeJwt } from "@/services/jwt";
import '@progress/kendo-ui'
import '@progress/kendo-ui/js/messages/kendo.messages.es-AR'
import '@progress/kendo-ui/js/cultures/kendo.culture.es-AR'
import '@progress/kendo-theme-bootstrap/dist/all.css'
import { DataSource } from '@progress/kendo-datasource-vue-wrapper'
import { Grid, GridColumn } from '@progress/kendo-grid-vue-wrapper'
import { Button } from '@progress/kendo-buttons-vue-wrapper'
import { directive as fullscreen } from 'vue-fullscreen'

export default {
  name: 'RowaPlanCanje',
  directives: {
    fullscreen,
  },
  components: {
    "grid": Grid,
    "grid-column": GridColumn,
    "datasource": DataSource,
    "kbutton": Button
  },
  data: function () {
    return {
      fullscreen: false,
      teleport: true,
      pageOnly: true,
      title: 'Lista de precios - Plan Canje por siempre ROWA',
      schemaModelFields: {
        Cliente_devuelve_mediante_NC: { type: 'string' },
        Valor_de_la_NC: { type: 'number' },
        Producto_nuevo_que_lleva_el_cliente: { type: 'string' },
        Precio_sin_Plan_Canje_L1_Cdo_Plataforma: { type: 'number' },
        Precio_con_plan_canje_que_paga_el_cliente: { type: 'number' },
        Descuento_incluido_en_precio_con_plan_canje: { type: 'number' }
      }
    }
  },
  computed: {
    token() {
      return decodeJwt(getToken()).token
    },
    UrlApiBase() {
      return `${process.env.VUE_APP_API_BASE}/rowaplancanje`
    },
    options() {
      return {
        callback: (isFullscreen) => {
          this.fullscreen = isFullscreen
        },
        target: '.directive-fullscreen-wrapper',
        pageOnly: this.pageOnly,
        teleport: this.teleport
      }
    }
  },
  methods: {
    readData: function (e) {
      // console.log(this.token)
      var token = this.token
      var urlApi = `${process.env.VUE_APP_API_BASE}/rowaplancanje`
      kendo.jQuery.ajax({
        url: urlApi,
        beforeSend: function (xhr) {
          xhr.setRequestHeader('Authorization', 'Bearer ' + token)
        },
        dataType: 'json',
        success: function (data) {
          e.success(data)
        },
        type: 'GET'
      })
    },
    toolbarTemplate: function () {
      var templateHtml =
        '<span id="form" style="position:absolute; left:5px">' +
        '<span>' +
        '<a class="k-pager-refresh k-link k-button refresh" title="Actualizar"><span class="k-icon k-i-reload"></span></a>' +
        '</span>' +
        '</span>';
      return templateHtml;
    },
  },
  mounted: function () {
    var grid = this.$refs.grid.kendoWidget();
    var gridElement = grid.element;
    var toolbarElement = gridElement.find('.k-grid-toolbar');
    toolbarElement.on("click", ".refresh", function (e) {
      e.preventDefault();
      grid.dataSource.read();
    });
  }
}
</script>

